public class TopStat {

    private String name;
    private Long value;

    public TopStat(String name, Long value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getValue() {
        return value;
    }

    public void setValue(Long value) {
        this.value = value;
    }

    public int compareDesc(TopStat topStat) {
        return -Long.compare(this.value, topStat.value);
    }

}
